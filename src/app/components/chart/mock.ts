export enum TrafficStateType {
  none = 'none',
  free = 'free', // 1
  normal = 'normal', // 2
  detectorFailure = 'detectorFailure', // 3,
  disturbance = 'disturbance', // 4
  heavy = 'heavy', // 5
  dense = 'dense', // 6
  slow = 'slow', // 7
  congestion = 'congestion', // 8
  critical = 'critical', // 9
}

enum Colors {
  good = '255,0,0',
  medium = '0,255,0',
  bad = '0,0,255',
}

export const ReportsUtils = {
  barChart5MinInterval: (1 / 60) * 5,
  tickHoursInterval: 2,
  getTrafficState(flow: number, speed: number): string {
    const detectorStrategicConditions: { [key in TrafficStateType]: boolean } =
      {
        none: flow === 0 && speed === 0,
        free: flow === 1 && speed <= 2,
        normal: flow === 2 && speed <= 2,
        detectorFailure: flow === 3 && speed === 1,
        disturbance: flow === 1 && speed === 3,
        critical: flow === 1 && speed === 4,
        heavy: flow === 3 && speed === 2,
        dense: flow === 2 && speed === 3,
        slow: flow === 3 && speed === 3,
        congestion: flow >= 2 && speed === 4,
      };

    const statusIndex = Object.values(detectorStrategicConditions).findIndex(
      (condition) => condition
    );
    return Object.keys(detectorStrategicConditions)[statusIndex];
  },
  getTrafficStateColor(
    status: TrafficStateType[keyof TrafficStateType]
  ): string {
    if (!status || status === TrafficStateType.none) {
      return '0, 0, 0';
    }

    return status === TrafficStateType.free ||
      status === TrafficStateType.normal
      ? Colors.good
      : status === TrafficStateType.heavy ||
        status === TrafficStateType.dense ||
        status === TrafficStateType.detectorFailure ||
        status === TrafficStateType.disturbance
      ? Colors.medium
      : Colors.bad;
  },
};

